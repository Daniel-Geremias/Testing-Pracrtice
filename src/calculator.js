const calculator = {
    add: function(n1, n2) {
            const sum = n1 + n2;
            return sum;
        },
    subtract: function(n1, n2) {
            const sub = n1 - n2;
            return sub;
        },
    divide: function(n1, n2) {
            const divi = n1 / n2;
            return divi;
        },
    multiply: function(n1, n2) {
            const multi = n1 * n2;
            return multi;
        }
};

export default calculator;